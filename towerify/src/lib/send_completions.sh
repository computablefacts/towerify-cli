## [@bashly-upgrade completions send_completions]
send_completions() {
  echo $'# towerify completion                                      -*- shell-script -*-'
  echo $''
  echo $'# This bash completions script was generated by'
  echo $'# completely (https://github.com/bashly-framework/completely)'
  echo $'# Modifying it manually is not recommended'
  echo $''
  echo $'_towerify_completions_filter() {'
  echo $'  local words="$1"'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local result=()'
  echo $''
  echo $'  # words the user already typed (excluding the command itself)'
  echo $'  local used=()'
  echo $'  if ((COMP_CWORD > 1)); then'
  echo $'    used=("${COMP_WORDS[@]:1:$((COMP_CWORD - 1))}")'
  echo $'  fi'
  echo $''
  echo $'  if [[ "${cur:0:1}" == "-" ]]; then'
  echo $'    # Completing an option: offer everything (including options)'
  echo $'    echo "$words"'
  echo $''
  echo $'  else'
  echo $'    # Completing a non-option: offer only non-options,'
  echo $'    # and don\'t re-offer ones already used earlier in the line.'
  echo $'    for word in $words; do'
  echo $'      [[ "${word:0:1}" == "-" ]] && continue'
  echo $''
  echo $'      local seen=0'
  echo $'      for u in "${used[@]}"; do'
  echo $'        if [[ "$u" == "$word" ]]; then'
  echo $'          seen=1'
  echo $'          break'
  echo $'        fi'
  echo $'      done'
  echo $'      ((!seen)) && result+=("$word")'
  echo $'    done'
  echo $''
  echo $'    echo "${result[*]}"'
  echo $'  fi'
  echo $'}'
  echo $''
  echo $'_towerify_completions() {'
  echo $'  local cur=${COMP_WORDS[COMP_CWORD]}'
  echo $'  local compwords=()'
  echo $'  if ((COMP_CWORD > 0)); then'
  echo $'    compwords=("${COMP_WORDS[@]:1:$((COMP_CWORD - 1))}")'
  echo $'  fi'
  echo $'  local compline="${compwords[*]}"'
  echo $''
  echo $'  COMPREPLY=()'
  echo $''
  echo $'  case "$compline" in'
  echo $'    *\'secrets remove\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --env --help -d -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'secrets unset\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --env --help -d -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'secrets list\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --env --help -d -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'completions\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--help -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'secrets set\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --env --help -d -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'secrets add\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --env --help -d -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'secrets rm\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --env --help -d -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'configure\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --domain --help --login --password --profile -d -h -l -p")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'secrets\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--help -h add list remove rm set unset")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'update\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --force --help -d -f -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'deploy\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --env --help --profile -d -e -h")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *\'init\'*)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --force --help -d -f -h ask laravel-10 laravel-9 static")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'    *)'
  echo $'      while read -r; do COMPREPLY+=("$REPLY"); done < <(compgen -W "$(_towerify_completions_filter "--debug --help --profile --version -d -h -v completions configure deploy init secrets update")" -- "$cur")'
  echo $'      ;;'
  echo $''
  echo $'  esac'
  echo $'} &&'
  echo $'  complete -F _towerify_completions towerify'
  echo $''
  echo $'# ex: filetype=sh'
}